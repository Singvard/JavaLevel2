public class Member {

    private int running;
    private int jumping;
    private int throwing;

    public Member() {}

    public int getRunning() {
        return this.running;
    }

    public void setRunning(final int running) {
        this.running = running;
    }

    public int getJumping() {
        return this.jumping;
    }

    public void setJumping(final int jumping) {
        this.jumping = jumping;
    }

    public int getThrowing() {
        return this.throwing;
    }

    public void setThrowing(final int throwing) {
        this.throwing = throwing;
    }
}
